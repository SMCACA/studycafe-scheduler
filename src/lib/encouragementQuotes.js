/**
 * 오늘 날짜를 기준으로 문구 하나를 골라줍니다.
 * - 같은 날에는 항상 같은 문구가 나와요 (새로고침해도 안 바뀜)
 * - 날짜가 바뀌면 다른 문구로 자동으로 바뀌어요
 */
export function getTodayQuote() {
  const d = new Date()
  const dateStr = `${d.getFullYear()}-${d.getMonth() + 1}-${d.getDate()}`

  // 날짜 문자열을 숫자로 바꿔주는 간단한 계산(해시)이에요.
  // 비유: 날짜를 "복권 번호"로 바꿔서, 그 번호로 오늘의 문구를 뽑는 느낌이에요.
  let hash = 0
  for (let i = 0; i < dateStr.length; i++) {
    hash = (hash * 31 + dateStr.charCodeAt(i)) % 1000000
  }

  const index = Math.abs(hash) % ENCOURAGEMENT_QUOTES.length
  return ENCOURAGEMENT_QUOTES[index]
}
